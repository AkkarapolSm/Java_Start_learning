class Prepost {
    public static void main(String[] arge){
        int a=5;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        
        a=5;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a+"\n");
        
        int b=4;
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);
        
        b=4;
        System.out.println(b);
        System.out.println(b--);
        System.out.println(b);
        

    }
}